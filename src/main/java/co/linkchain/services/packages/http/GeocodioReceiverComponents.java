package co.linkchain.services.packages.http;

public class GeocodioReceiverComponents {
    private String number;
    private String street;
    private String suffix;
    private String postdirectional;
    private String formattedStreet;
    private String city;
    private String county;
    private String state;
    private String zip;
    private String country;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getSuffix() {
        return suffix;
    }

    public void setSuffix(String suffix) {
        this.suffix = suffix;
    }

    public String getPostdirectional() {
        return postdirectional;
    }

    public void setPostdirectional(String postdirectional) {
        this.postdirectional = postdirectional;
    }

    public String getFormatted_street() {
        return formattedStreet;
    }

    public void setFormatted_street(String formattedStreet) {
        this.formattedStreet = formattedStreet;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }
}
